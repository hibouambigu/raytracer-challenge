#include "materials.h"


////////////////////////////////////////////////////////////////////////////////////////////////////
/// Material
////////////////////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////////////////////////
Material::Material()
:   colour(Colour{1, 1, 1}),
    ambient(0.1),
    diffuse(0.9),
    specular(0.9),
    shininess(200.0)
{}

////////////////////////////////////////////////////////////////////////////////////////////////////
bool operator==(const Material& a, const Material& b)
{
    return    a.colour == b.colour
           && a.ambient == b.ambient
           && a.diffuse == b.diffuse
           && a.specular == b.specular
           && a.shininess == b.shininess;
}
