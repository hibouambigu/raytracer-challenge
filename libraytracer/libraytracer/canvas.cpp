#include <canvas.h>

////////////////////////////////////////////////////////////////////////////////////////////////////
Canvas::Canvas(size_t width, size_t height)
: width(width), height(height), pixels(width, height)
{
    std::cout << "matrix is blank? " << (pixels.isBlank() ? "TRUE" : "FALSE") << "\n";
}

////////////////////////////////////////////////////////////////////////////////////////////////////
size_t Canvas::getWidth()
{
    return width;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
size_t Canvas::getHeight()
{
    return height;
}
